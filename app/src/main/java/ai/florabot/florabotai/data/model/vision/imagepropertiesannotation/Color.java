package ai.florabot.florabotai.data.model.vision.imagepropertiesannotation;

public class Color {

    int red;
    int green;
    int blue;

    public Color() { }

    public int getRed() {
        return red;
    }

    public void setRed(int red) {
        this.red = red;
    }

    public int getGreen() {
        return green;
    }

    public void setGreen(int green) {
        this.green = green;
    }

    public int getBlue() {
        return blue;
    }

    public void setBlue(int blue) {
        this.blue = blue;
    }
}
